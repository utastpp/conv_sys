    private void sendQueryButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendQueryButtonActionPerformed
        //System.out.println("======================Sending Message");
        if (!this.usernameTextField.getText().equals("") && !this.query.getText().trim().equals("")) {
            username = this.usernameTextField.getText();
            String theQuery = this.query.getText();
            parent.setManualMessageGUI(username, theQuery, this);
       }
       else if (this.usernameTextField.getText().equals(""))
            JOptionPane.showMessageDialog(this,"User name cannot be empty");
       else if (this.query.getText().trim().equals(""))
            JOptionPane.showMessageDialog(this,"Query cannot be empty");

        
    }//GEN-LAST:event_sendQueryButtonActionPerformed

