    /**
     *
     * @param reply
     */
    public void setReply(String reply) {
        DateFormat dateFormat = new SimpleDateFormat("HH:mm:ss");
        Calendar cal = Calendar.getInstance();
        this.response.setText(reply);
        this.reply.insert(dateFormat.format(cal.getTime()) + "\tIDS: " + reply + "\n\t\t" + username + ": " + this.query.getText() + "\n", 0);
        this.reply.setCaretPosition(0);
        this.query.setText("");
       
    }
    
    
